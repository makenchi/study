int alterar (TLista L, int velho, int novo)
{
	TLista aux = L;
	int cont = 0;
	
	while (aux)  //while (aux != NULL)
	{
		if (aux->valor == velho)
		{
			aux->valor = novo;
			cont++;
		}
		aux = aux->prox;
	}
	
	if (cont == 0)
	{
		return FALSE;
	}
	else
	{
		return TRUE;
	}
	
	//return cont;
}

int buscar (TLista L, int numero)
{
	TLista aux = L;

	while (aux)
	{
		if (aux->valor == numero)
		{
			return TRUE;
		}
		
		aux = aux->prox;
	}	
	
	return FALSE;
}

